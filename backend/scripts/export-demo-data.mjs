/**
 * Export the dev tables into a self-contained browser dataset for Demo Mode.
 * Produces ../demo/data.js defining window.HSG_DEMO_DATA — the same sales,
 * pools, and loans the live API serves, so the no-login demo is pixel-identical
 * to production behavior.
 *
 *   AWS_PROFILE=hsg-hudoas node scripts/export-demo-data.mjs
 */
import { DynamoDBClient } from '@aws-sdk/client-dynamodb';
import { DynamoDBDocumentClient, ScanCommand } from '@aws-sdk/lib-dynamodb';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const region = 'us-east-1';
const ddb = DynamoDBDocumentClient.from(new DynamoDBClient({ region }));

async function scanAll(TableName) {
  const items = [];
  let ExclusiveStartKey;
  do {
    const r = await ddb.send(new ScanCommand({ TableName, ExclusiveStartKey }));
    items.push(...(r.Items || []));
    ExclusiveStartKey = r.LastEvaluatedKey;
  } while (ExclusiveStartKey);
  return items;
}

// Scrub legacy labels from string VALUES (keys untouched): the invented
// "HUD-90092" form number was corrected platform-wide to "Commercial QS".
function scrub(v) {
  if (typeof v === 'string') return v.replace(/HUD-90092/g, 'Commercial QS').replace(/\b90092\b/g, 'Commercial QS');
  if (Array.isArray(v)) return v.map(scrub);
  if (v && typeof v === 'object') {
    const out = {};
    for (const k of Object.keys(v)) out[k] = scrub(v[k]);
    return out;
  }
  return v;
}

const sales = scrub(await scanAll('hsg-dev-sales'));
const loans = scrub(await scanAll('hsg-dev-loans'));
const qc = scrub(await scanAll('hsg-dev-qc-findings'));

