const target = path.resolve(__dirname, '../../demo/data.js');
fs.mkdirSync(path.dirname(target), { recursive: true });
fs.writeFileSync(target, '/* Generated by backend/scripts/export-demo-data.mjs — DO NOT EDIT BY HAND */\nwindow.HSG_DEMO_DATA = ' + JSON.stringify(out, null, 1) + ';\n');
